    <h3 class="font-semibold underline">Event Participation</h3>

    <div class="mb-2">
        The Event Participation application is used by the overwhelming majority of
        TheDirectorsRoom.com teachers to:
        <ul class="ml-8 list-disc">
            <li>register their students for upcoming events managed on TheDirectorsRoom.com,</li>
            <li>review the student audition results of past events, and</li>
            <li>adjudicate events for which they are assigned judges.</li>
        </ul>
    </div>

    <div>

        <p>The Events Participation application has two action: GoTo and Results.</p>

        {{-- GOTO --}}
        <div class="ml-8 mb-4">
            <h4 class="font-semibold underline text-yellow-200">GoTo</h4>
            <p class="mb-2">
                The GoTo action is used by open events to allow teachers to register their students
                for the open event and/or review the input from students who self-register for an
                open event via the StudentFolder.info application.
            </p>

            <p>
                The GoTo action has four sections:
            </p>
            <ul class="ml-8 list-disc text-sm">
                <li>
                    <span class="text-yellow-200">Candidate Roster</span>: Review the current status
                    of all eligible students for the open event.
                </li>
                <li>
                    <span class="text-yellow-200">Teacher Obligations</span>: Review and confirm the
                    obligations required of teachers participating in the event.
                </li>
                <li>
                    <span class="text-yellow-200">Pitch Files</span>: Review and play any audition
                    pitch files available for the event.
                </li>
                <li>
                    <span class="text-yellow-200">Estimate/Invoice</span>: Review any estimate/invoice
                    form available for the event, review/record payments made by students manually or
                    through epayment systems (ex. PayPal), make payments through epayment systems
                    (ex. PayPal).
                </li>
            </ul>
        </div>

        {{-- RESULTS --}}
        <div class="ml-8 mb-4">
            <h4 class="font-semibold underline text-yellow-200">Results</h4>
            <p class="mb-2">
                The GoTo action is used by closed events to allow teachers to review the student
                audition results after the event closes.
            </p>

            <p class="mb-2">
                In addition to the visual review, individual, school, and event-wide summary results
                may be available via downloadable pdf if authorized by the event.
            </p>
        </div>


    </div>
</div>
